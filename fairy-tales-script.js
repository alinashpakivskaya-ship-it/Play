    playerAnswer.textContent = gameState.currentQuestion.answers[gameState.selectedAnswer];
    correctAnswer.textContent = `–ü—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç: ${gameState.currentQuestion.answers[gameState.currentQuestion.correct]}`;
    
