        const token = document.getElementById('player-token');
        if (token) {
            token.remove();
        }
        
        document.querySelectorAll('.shop-item').forEach(item => {
            item.classList.remove('bought');
        });
        
        updatePlayersDisplay();
        startNewTurn();
        
        showMessage('üîÑ –ù–∞—á–∞—Ç–∞ –Ω–æ–≤–∞—è –∏–≥—Ä–∞!', 'success');
