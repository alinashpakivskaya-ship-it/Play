        gameState.players.forEach(player => {
            player.coins = 0;
            player.pieces = { king: 0, queen: 0, rook: 0, bishop: 0, knight: 0, pawn: 0 };
        });
        gameState.currentPlayerIndex = 0;
        gameState.questionsAnswered = 0;
        gameState.startTime = new Date();
