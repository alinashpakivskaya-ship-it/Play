    if (currentPlayer.items[itemType] > 0) {
        showMessage('–¢—ã —É–∂–µ –∫—É–ø–∏–ª —ç—Ç–æ—Ç –ø—Ä–∏–∑!', 'warning');
        return;
    }
    
